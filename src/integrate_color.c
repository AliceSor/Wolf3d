int				integrate_color(double r, double g, double b)
{
	int			res;

	r = (r < 255) ? r : r/255;
	g = (g < 255) ? g : g/255;
	b = (b < 255) ? b : b/255;
    r = (r > 0) ? r : 0;
    g = (g > 0) ? g : 0;
    b = (b > 0) ? b : 0;
	res = ((int)r << 16) + ((int)g << 8) + (int)b;
	return (res);
}
